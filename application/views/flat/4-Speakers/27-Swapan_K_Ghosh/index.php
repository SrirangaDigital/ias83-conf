                <p class="title">Density functional theory for materials modelling at different length scales: A legacy of Walter Kohn</p>
                <p class="abstract">
                    One of the concepts that has played a major role in the conceptual as well as computational
                    developments covering different length scales of interest in a number of areas of chemistry,
                    physics and materials science is single-particle density. The use of single-particle electron
                    density as the basic variable was formally established by Walter Kohn <em>et al.,</em> leading to
                    the birth of density functional theory (DFT), which has established itself as a conceptually
                    simple, computationally economic and versatile tool for electronic structure-based calculations
                    and understanding of chemical binding in atoms, molecules and solids. DFT has also
                    travelled beyond the territory of the microscopic length scale and quantum domain. Thus,
                    in the intermediate mesoscopic length scale, which covers a wide class of problems involving
                    nanomaterials, interfacial science and soft condensed matter, an appropriate picture
                    of the equilibrium and dynamical processes has been obtained through the single-particle
                    number density of the constituent particles. In the macroscopic length scale, however, one
                    usually considers matter as a continuous medium, and a description using local mass density,
                    energy density and other related property density functions has been found to be quite
                    appropriate. In spite of the differences in the nature of the density variables used in all
                    these DFT-based descriptions, the corresponding theoretical frameworks, which project the
                    many-particle picture to a single-particle one, have been found to possess an underlying
                    unified structure. Some of these developments and applications of DFT in diverse areas of
                    chemistry and physics will be discussed.
                </p>
