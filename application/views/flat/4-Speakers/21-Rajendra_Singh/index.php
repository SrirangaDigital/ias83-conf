                <p class="title">Indigenous knowledge system of water management in India</p>
                <p class="abstract">
                    In India, traditionally, knowledge was transmitted through practical work under the direction
                    of respected elders and gurus. Thus, the people engaged in practical work were really
                    the pupils of an indigenous knowledge system. All pupils, poor and prosperous, and the
                    State joined hands in the conservation of water and the preservation of knowledge. Prosperous
                    pupils provided help to the poorest ones, and the State provided the land. Indigenous
                    water management was thus a pupil-driven decentralized system.
                </p>
                <p class="abstract">
                    This indigenous knowledge system had acknowledged the diversity in agro-ecological zones
                    even those that seems inhospitable, and had developed a specific science, a relevant engineering
                    and a technology appropriate to each part of the country to enable living in a
                    sustainable manner with Nature.
                </p>
                <p class="abstract">
                    Since this technology, perfected by tradition, was transmitted orally and practically, this
                    ancient indigenous engineering was not well documented. However, in some cases, the
                    legal and administration aspects were documented: for example, in Kautilyas Arthasastra,
                    one chapter is devoted to a very comprehensive and detailed administrative rules, covering
                    the whole range of legal and economic implications of a decentralized community-driven
                    water management, facilitated by the State.
                </p>
                <p class="abstract">
                    The objective of the talk is to showcase indigenous knowledge systems and their contributions
                    to development of sustainable water management strategies in the light of community
                    development initiatives and also to share ideas as contemporary development issues in water
                    management.
                </p>
