                <p class="abstract">
                    The formation of charge transfer states in a binary quantum dot (QD) mixture will be
                    demonstrated. Charge is observed to reside in quantum confined states of one of the participating
                    QDs. Charge transfer gives rise to electrostatic forces between QD pairs, leading
                    to materials that may be regarded as the nanoscale analogs of an ionic solid. These solids
                    are shown to lack crystalline order, but exhibit a well-defined stoichiometry. The formation
                    mechanisms of these materials and the reasons behind the emergence of atom-like characteristics
                    in QDs will be described. Finally, various examples of chemical reactions between
                    QDs will be provided and also the energetics of such reactions will be described.
