                <p class="abstract">
                    The microbial species which reside on the human body, play an important role in many
                    biological processes. A number of body parts harbor a variety of microbial species of nonhuman
                    origin. The number of microbial species are thought to outnumber the total number
                    of human cells in the body. Any change in the composition of such microbiota has been
                    shown to lead to different pathological conditions in the recent past. A number of fundamental
                    questions therefore remain to be addressed on the effect of resident microbiota on
                    human health. It also remains to be known if the microbiota in different geographical regions
                    of the world, and in different ethnic populations, differ significantly. To address this
                    question, it is proposed to initiate a large-scale study on the Indian population and microbiota
                    harbored by this population. Five different body sites are proposed to be explored for
                    the mapping of the microbiota. Study design and the ambitious plan of how this is proposed
                    to be coordinated will be presented.
