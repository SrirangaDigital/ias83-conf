                <p class="abstract">
                    Over the last two decades, low temperature scanning tunneling spectroscopy has emerged
                    as one of the most powerful tools to study electronic properties of solids at low temperature,
                    with unsurpassed spatial and energy resolution. In this talk, the basics of this technique and
                    the low temperature scanning tunneling microscope developed in TIFR, operating down
                    to 350 mK in a field up to 90 kOe, will be described. Using specific examples from the
                    studies carried out in the speaker’s laboratory, the kind of problems that can be addressed
                    using this technique will be highlighted. That it is much more economic and infinitely
                    more productive to build your own scanning tunneling microscope than to buy commercial
                    products commonly available in the market will be shown.
