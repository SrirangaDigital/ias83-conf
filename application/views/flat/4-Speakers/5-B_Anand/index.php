                <p class="title">Homing mechanism of the CRISPR-Cas adaptive immune system</p>
                <p class="abstract">
                    The CRISPR-Cas system represents genetically encoded prokaryote-specific adaptive defence
                    machinery that limits the genome invasion by mobile genetic elements such as bacteriophages
                    and plasmids by employing an RNA-mediated interference mechanism. This
                    system comprises a non-coding CRISPR array and a CRISPR-associated (cas) operon. The
                    CRISPR array comprises direct repeats of about 3040 nts that are intervened by similarly
                    sized variable DNA referred as spacers that are derived from invading foreign DNA. The
                    cas operon encodes several Cas proteins that together with the CRISPR RNA form a ribonucleoprotein
                    surveillance complex. In this complex, the RNA guides the target recognition
                    by base complementarity, whereas the proteins facilitate the target cleavage.
                </p>
                <p class="abstract">           
                    The cornerstone of this immune system is that it confers heritable adaptive immunity to
                    bacteria by capturing a protospacer fragment from invading foreign DNA, which is later inserted
                    into the leader proximal end of CRIPSR array and serves as immunological memory
                    to recognise recurrent invasions, akin to antibodies in the case of higher organisms. The
                    universally conserved Cas1 and Cas2 form an integration complex that is known to mediate
                    the protospacer invasion into the CRISPR array. However, the mechanism by which this
                    protospacer fragment gets integrated in a directional fashion into the leader proximal end
                    is elusive. The speakers group identified that the leader region abutting the first CRISPR
                    repeat localizes Integration Host Factor (IHF) and Cas1-2 complex in <em>Escherichia coli.</em> IHF
                    binding to the leader region induces bending by about 120°, which in turn engenders the regeneration
                    of the cognate binding site for protospacer-bound Cas1-2 complex and brings it
                    in proximity with the first CRISPR repeat. This appears to guide Cas1-2 complex to orient
                    the protospacer invasion towards the leader-repeat junction thus driving the integration in
                    a polarised fashion. Their work thus illuminates how Cas1-2 complex and IHF coordinate
                    bring about the preferential integration of protospacer into the leader proximal end.
                </p>
