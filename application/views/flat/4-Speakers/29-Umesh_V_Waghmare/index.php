                <p class="title">Electrons in a crystal: Localized versus extended orbitals</p>
                <p class="abstract">
                    In a crystal, an electron with a definite energy behaves like a wave that extends spatially
                    through the crystal, and is described by an extended Bloch wave-function. Interestingly,
                    the electronic structure of a crystal exhibits a near-sightedness, which is conceptually underlying
                    the nature of chemical bonding in it. Its precise and local picture is obtained from
                    the construct of Wannier functions, the localized orbitals that form the basis for development
                    of (a) simple models to capture the essential physics, differentiating between metals
                    and insulators, (b) linearly scaling efficient computational methods to determine electronic
                    structure, and (c) a picture of chemical bonding. These ideas apply quite well to dynamical
                    structure of a crystal, as described by the dispersion of its phonons or vibrational waves.
                    The speakers group has shown an interesting relationship between Wannier functions and
                    non-Abelian Berry phases of Bloch electrons, which is centrally relevant to the nontrivial
                    electronic topology exhibited by topological insulators, Weyl and Dirac semimetals.
                </p>                
