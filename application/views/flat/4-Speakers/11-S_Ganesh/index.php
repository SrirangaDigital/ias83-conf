                <p class="title">Role of non-coding RNAs in cellular stress response</p>
                <p class="abstract">
                    The heat shock response (HSR) is a conserved cellular defence mechanism that ensures
                    cell survival during a thermal stress. One of the unique features of human cells is the expression
                    of the satellite-III family (Sat3) long non-coding RNAs during the HSR. The Sat3
                    transcripts accumulate at the site of their transcription upon expression and recruit transcription
                    factors to form a RNAâ€“protein complex, called the nuclear stress bodies (nSBs),
                    with unknown function. We find that Sat3 transcripts are required for the full protection
                    from heat-shock-induced cell death, and that the loss of these transcripts leads to a partial
                    relief of heat-shock-induced transcriptional repression. In this talk these findings will
                    be contrasted with other such regulatory RNAs known in the literature, and their role in
                    generic stress response pathways will be discussed.
