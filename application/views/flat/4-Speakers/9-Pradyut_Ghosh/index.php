                <p class="abstract">
                    Anion recognition by synthetic receptors has received increasing attention for its importance
                    in environment, industrial and health-related issues. Thus, the fundamental chemistry
                    of selective recognition, sensing and extraction of various anions in aqueous medium
                    are considered to be very important to develop technologies in water purification process
                    and industrial and nuclear waste reprocessing. A successful approach for developing receptors
                    that selectively bind fluoride, cyanide, arsenate, nitrate, sulfate, phosphates, etc.,
                    in aqueous medium is one of the major challenges in anion chemistry. In this regard, a
                    wide variety of receptors (ligands) containing different anion recognition elements with
                    increasing complexities have been developed by the speakers team, which will be briefly
                    presented in this talk. The focus will be on their recent work which deals with (i) selective
                    sensing of phosphates; (ii) separation of hydrated metal sulphates/potassium bromide; and
                    (iii) halogen-bonding-assisted bromide removal.
