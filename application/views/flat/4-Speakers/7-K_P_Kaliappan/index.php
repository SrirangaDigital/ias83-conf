                <p class="title">Domino strategy to synthesis of natural products and heterocycles</p>
                <p class="abstract">
                    Domino and multicomponent reactions are always attractive and they are expected to provide
                    the target molecules efficiently in a shortest possible route. Our group has been engaged
                    in designing simple and efficient domino strategies for the syntheses of biologically
                    active natural products and natural product-like molecules. In this lecture, our efforts leading
                    to syntheses of vinigrol, cyclic guanidines and N-heterocyclic amides will be discussed
                    in details. Vinigrol, a unique diterpene containing the decahydro-1,5-butanonaphthalene
                    carbon skeleton was shown to exhibit a broad spectrum of biological activity. Besides the
                    multiple sites of oxygenation, vinigrol contains a tricyclic core having a cis-fused [4.4.0]
                    system bridged by an eight-membered ring and eight contiguous stereocenters. We recently
                    reported an enantioselective formal synthesis of vinigrol involving a 1-2-3-4 strategy: one
                    pot and 2-reactions with the formation of 3-rings and 4-bonds leading to the core structure
                    of vinigrol from its stereochemically well-defined acyclic precursor. The cyclic guanidines
                    and N-heterocyclic amides are important structural units present in biologically active drug
                    molecules. However, the existing methods suffer from harsh conditions, narrow functional
                    group tolerance, poor atom economy, low yielding and so it warrants an efficient protocol
                    for their syntheses. We have developed a one-pot Cu-catalyzed cascade routes to these
                    unique cyclic guanidines and N-heterocyclic amides from readily available starting materials.
                </p>
