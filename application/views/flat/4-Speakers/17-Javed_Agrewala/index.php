                <p class="title">Caerulomycin A suppresses arthritis symptoms by expanding regulatory T cells</p>
                <p class="abstract">
                    Caerulomycin A is a bipyridyl compound produced by actinomycetes and is known to exhibit
                    antifungal and antibiotic properties. The role of Caerulomycin A as an immunosuppressive
                    agent in alleviating the arthritis symptoms by generating regulatory T cells (Tregs)
                    was exploited in our studies. It was observed that Caerulomycin A substantially augmented
                    the percentage of Tregs. Further, it suppressed the number of Th1 cells and Th17 cells. Furthermore,
                    the mechanism was established and that Caerulomycin A interfered with IFN-<em>γ</em>
                    induced STAT1 signalling by augmenting SOCS1 expression was observed. An increase in
                    the TGF-<em>β</em> mediated Smad3 activity was also noted. Caerulomycin A rescued Tregs from
                    IFN-<em>γ</em> induced inhibition. In essence, our results suggest a novel role of Caerulomycin A in
                    the formation of Tregs. This finding proposes that Caerulomycin A has sufficient potential
                    to be considered as a future drug to treat arthritis.
