                <p class="abstract">
                    Marvelous world-class experiments involving neutrinos from the Sun, the Earth’s atmosphere,
                    nuclear reactors and accelerators have firmly established neutrino flavour oscillations.
                    This implies that neutrinos have mass and they mix with each other, providing
                    evidence for physics beyond the Standard Model. The most recent development in this
                    field is the discovery of the smallest lepton mixing angle <em>θ<sub>13</sub></em>. This signifies an important
                    breakthrough in validating the standard three-flavour oscillation picture of neutrinos. In
                    this talk, the present status of the neutrino oscillation parameters will be reviewed, and
                    the scope of addressing the remaining fundamental unknowns, in particular, the type of
                    the neutrino mass hierarchy, the possible presence of a CP violating phase in the neutrino
                    sector, and octant degeneracy of <em>θ<sub>23</sub></em> in current and future long-baseline neutrino oscillation
                    experiments will be discussed. The role of the upcoming India-based Neutrino Observatory
                    (INO) project to resolve the fundamental issue of neutrino mass ordering will be mentioned.
